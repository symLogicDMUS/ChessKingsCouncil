def print_fen_board(board):
    """take the board dict and print a board of fen_ids"""
    for y in range(8, 0, -1):
        for x in range(1, 9):
            if board[x, y] != '#':
                id_ = get_fen_id(get_piece_type(board[x, y]), get_color(board[x, y]))
            else:
                id_ = '#'
            print(id_, end=' ')
            if x % 8 == 0:
                print('')


