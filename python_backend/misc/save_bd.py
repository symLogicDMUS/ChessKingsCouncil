

def save_bd(board_dict):
    """to help the programmer. saves the board dict ordered in desirable way"""
    board_str = "{"
    for y in range(8, 0, -1):
        for x in range(1, 9):
            board_str += "({}, {}): '{}'".format(x, y, board_dict[x, y])
            if (x, y) == (8, 1):
                board_str += '}'
            else:
                board_str += ', '
        board_str += "\n"
    f = open("bd.txt", "w")
    f.write(board_str)


