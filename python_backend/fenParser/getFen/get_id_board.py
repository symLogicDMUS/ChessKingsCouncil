def get_id_board(board):
    """getFen TASK 1, takes board as input, and return board as list of lists where each list is a row"""
    id_board = []
    row = []
    for y in range(1, 9):
        for x in range(1, 9):
            row.append(board[x, y])
        id_board.insert(0, row)
        row = []
    return id_board


